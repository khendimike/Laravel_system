     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'stationname'=>'required',
            'registeredvoters'=>'required',
        ]);

        $pollingStationData = new PollingStations;
        $pollingStationData->polling_station = $request->get('stationname');
        $pollingStationData->registered_voters = $request->get('registeredvoters');
        
        $pollingStationData->save();
    }

